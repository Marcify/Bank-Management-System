    private void btnDepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositActionPerformed
        String text = tfAmount.getText().trim();
        
        if (text.isEmpty()) {
            System.out.println("This is text: " + text);
            JOptionPane.showMessageDialog(null, "Please enter an amount!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        double amount;
        
        try {
            amount = Double.parseDouble(text);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter a valid number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (amount <= 0) {
            JOptionPane.showMessageDialog(null, "Please enter a valid amount!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
//        if (amount > account.getBalance()) {
//            JOptionPane.showMessageDialog(null, "Insufficient balance!", "Error", JOptionPane.ERROR_MESSAGE);
//            return;
//        }
        
        account.deposit(amount);
        JOptionPane.showMessageDialog(null, "Deposit of â‚±" + amount + " successful!\nNew Balance: " + account.getBalance(), "Success", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_btnDepositActionPerformed

